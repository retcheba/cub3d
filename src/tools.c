#include "../inc/cub3d.h"

void	my_mlx_pixel_put(t_img_data	*data, int x, int y, int color)
{
	char	*dst;

	dst = data->addr + (y * data->line_length + x * (data->bpp / 8));
	*(unsigned int *)dst = color;
}
