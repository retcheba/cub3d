#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != (unsigned char)c && s[i])
		i++;
	if (!s[i] && (unsigned char)c != 0)
		return (NULL);
	return ((char *)s + i);
}
