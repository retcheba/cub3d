#include "ft_printf.h"

void	ft_putnbr(long nb, int *len)
{
	char	temp;

	temp = 0;
	if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
		*len += 1;
	}
	if (nb >= 10)
		ft_putnbr((nb / 10), len);
	temp = (nb % 10) + '0';
	*len += write (1, &temp, 1);
}
