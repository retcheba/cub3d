#include "libft.h"

int	ft_isalnum(int c)
{
	int				a;
	unsigned char	ch;

	a = 0;
	ch = c;
	if ((ch >= '0' && ch <= '9') || (ch >= 'A' && ch <= 'Z')
		|| (ch >= 'a' && ch <= 'z'))
	{
		a = 8;
	}
	return (a);
}
