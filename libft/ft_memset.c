#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*(unsigned char *)s = c;
		s++;
		i++;
	}
	while (i > 0)
	{
		s--;
		i--;
	}
	return (s);
}
